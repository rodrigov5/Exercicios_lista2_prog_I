dia_semana = int(input('Digite o dia de 1 a 7 :'))

if dia_semana == 2:
    print('Seu numero corresponde a segunda-feira')
elif dia_semana == 3:
    print('Seu numero corresponde a terça-feira')
elif dia_semana == 4:
    print('Seu numero corresponde a quarta-feira') 
elif dia_semana == 5:
    print('Seu numero corresponde a quinta-feira') 
elif dia_semana == 6:
    print('Seu numero corresponde a sexta-feira') 
elif dia_semana == 7:
    print('Seu numero corresponde a sábado') 
elif dia_semana == 1:
    print('Seu numero corresponde a Domingo') 
else:
    print('Número inválido') 